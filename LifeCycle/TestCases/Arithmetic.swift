import Foundation

class Arithmetic{
    
    func additionOperation(firstNumber: Int, secondNumber: Int) -> Int{
        let result = firstNumber + secondNumber
        return result
    }
    
    func subtractionOperation(firstNumber: Int, secondNumber: Int) -> Int{
        let result = firstNumber - secondNumber
        return result
    }
    
    func multiplicationOperation(firstNumber: Int, secondNumber: Int) -> Int{
        let result = firstNumber * secondNumber
        return result
    }
    
    func divisionOperation(firstNumber: Int, secondNumber: Int) -> Int{
        let result = firstNumber / secondNumber
        return result
    }

    
}
